// Fun√ß√£o de teste de volume
function testVoiceVolume() {
    console.log('üß™ Iniciando teste de volume...');
    
    const testPhrases = [
        "Teste de volume n√≠vel 1 - Volume baixo",
        "Teste de volume n√≠vel 2 - Volume m√©dio",
        "Teste de volume n√≠vel 3 - Volume alto"
    ];
    
    const volumes = [0.3, 0.7, 1.0];
    
    // Cancelar qualquer fala em andamento
    window.speechSynthesis.cancel();
    
    let index = 0;
    
    function speakNext() {
        if (index >= testPhrases.length) {
            console.log('‚úÖ Teste de volume conclu√≠do');
            
            // Testar com o sistema Delphos se dispon√≠vel
            if (window.voiceSystem) {
                console.log('üéØ Testando com sistema Delphos...');
                setTimeout(() => {
                    window.voiceSystem.speak("Este √© um teste final com o sistema Delphos em modo normal", false);
                }, 1000);
            }
            return;
        }
        
        const text = testPhrases[index];
        const volume = volumes[index];
        
        console.log(`üîä Teste ${index + 1}: Volume ${volume}`);
        
        const utterance = new SpeechSynthesisUtterance(text);
        utterance.volume = volume;
        utterance.lang = 'pt-BR';
        utterance.rate = 1.0;
        utterance.pitch = 1.0;
        
        utterance.onend = () => {
            index++;
            setTimeout(speakNext, 500);
        };
        
        utterance.onerror = (event) => {
            console.error('‚ùå Erro no teste:', event.error);
        };
        
        window.speechSynthesis.speak(utterance);
    }
    
    speakNext();
}

// Fun√ß√£o para verificar e corrigir problemas de √°udio do sistema
function checkSystemAudioIssues() {
    console.log('üîç Verificando problemas de √°udio do sistema...');
    
    // Verificar se o √°udio est√° mutado
    if (navigator.mediaDevices && navigator.mediaDevices.enumerateDevices) {
        navigator.mediaDevices.enumerateDevices()
            .then(devices => {
                const audioOutputs = devices.filter(device => device.kind === 'audiooutput');
                console.log('üîä Dispositivos de sa√≠da de √°udio:', audioOutputs);
                
                if (audioOutputs.length === 0) {
                    console.error('‚ùå Nenhum dispositivo de sa√≠da de √°udio encontrado!');
                    alert('Nenhum dispositivo de sa√≠da de √°udio foi detectado. Verifique suas configura√ß√µes de som.');
                }
            })
            .catch(err => {
                console.error('‚ùå Erro ao enumerar dispositivos:', err);
            });
    }
    
    // Criar um teste de √°udio simples
    try {
        const audioContext = new (window.AudioContext || window.webkitAudioContext)();
        const oscillator = audioContext.createOscillator();
        const gainNode = audioContext.createGain();
        
        oscillator.connect(gainNode);
        gainNode.connect(audioContext.destination);
        
        // Tom de teste muito breve
        oscillator.frequency.value = 440;
        gainNode.gain.value = 0.1;
        
        oscillator.start();
        oscillator.stop(audioContext.currentTime + 0.1);
        
        console.log('‚úÖ AudioContext funcionando corretamente');
    } catch (error) {
        console.error('‚ùå Erro ao testar AudioContext:', error);
    }
}

