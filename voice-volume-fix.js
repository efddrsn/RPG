// Fun√ß√£o para garantir que o volume esteja sempre configurado corretamente
function ensureVolumeSettings() {
    // Verificar se o sistema de s√≠ntese est√° dispon√≠vel
    if (window.speechSynthesis) {
        console.log('üîä Aplicando corre√ß√µes de volume...');
        
        // For√ßar carregamento de vozes
        const voices = window.speechSynthesis.getVoices();
        if (voices.length === 0) {
            console.log('‚è≥ Aguardando carregamento de vozes...');
            window.speechSynthesis.addEventListener('voiceschanged', ensureVolumeSettings);
            return;
        }
        
        console.log(`‚úÖ ${voices.length} vozes dispon√≠veis`);
        
        // Verificar vozes PT-BR
        const ptBRVoices = voices.filter(v => v.lang.includes('pt-BR'));
        console.log(`üáßüá∑ ${ptBRVoices.length} vozes PT-BR encontradas:`, 
            ptBRVoices.map(v => ({ name: v.name, default: v.default }))
        );
    }
    
    // Patch para a classe DelphosVoiceSystem se ela existir
    if (typeof DelphosVoiceSystem !== 'undefined') {
        console.log('üîß Aplicando patch no DelphosVoiceSystem...');
        
        // Salvar refer√™ncia do m√©todo original
        const originalSpeak = DelphosVoiceSystem.prototype.speak;
        
        // Sobrescrever o m√©todo speak
        DelphosVoiceSystem.prototype.speak = async function(text, isUnrestricted = false) {
            console.log('üéØ [PATCH] Interceptando speak:', {
                text: text.substring(0, 50) + '...',
                isUnrestricted,
                ttsMode: this.ttsMode,
                voiceMode: this.voiceMode
            });
            
            // Se for TTS nativo e modo normal, garantir volume m√°ximo
            if (this.ttsMode === 'native' && !isUnrestricted) {
                console.log('üì¢ [PATCH] For√ßando configura√ß√µes de volume para voz normal');
                
                // Criar uma fun√ß√£o wrapper para processar utterances
                const processUtteranceWithVolume = (utterance) => {
                    // Garantir volume m√°ximo
                    utterance.volume = 1.0;
                    
                    // Log detalhado
                    console.log('üîä [PATCH] Configura√ß√µes da utterance:', {
                        volume: utterance.volume,
                        pitch: utterance.pitch,
                        rate: utterance.rate,
                        voice: utterance.voice?.name || 'Padr√£o',
                        lang: utterance.lang
                    });
                    
                    // Adicionar listener para debug
                    const originalOnStart = utterance.onstart;
                    utterance.onstart = (event) => {
                        console.log('üé§ [PATCH] Utterance iniciada com volume:', utterance.volume);
                        if (originalOnStart) originalOnStart(event);
                    };
                    
                    return utterance;
                };
                
                // Salvar refer√™ncia do processUtteranceQueue original
                const originalProcessQueue = this.processUtteranceQueue;
                
                // Temporariamente sobrescrever processUtteranceQueue
                this.processUtteranceQueue = function() {
                    console.log('üîÑ [PATCH] Processando fila com corre√ß√£o de volume');
                    
                    // Verificar e corrigir volume de todas as utterances na fila
                    this.utteranceQueue = this.utteranceQueue.map(processUtteranceWithVolume);
                    
                    // Chamar m√©todo original
                    originalProcessQueue.call(this);
                };
            }
            
            // Chamar m√©todo original
            return originalSpeak.call(this, text, isUnrestricted);
        };
        
        console.log('‚úÖ Patch aplicado com sucesso');
    }
    
    // Verificar configura√ß√µes do Eleven Labs
    if (typeof ElevenLabsTTS !== 'undefined' || typeof ElevenLabsEnhanced !== 'undefined') {
        console.log('üîç Verificando configura√ß√£o do Eleven Labs...');
        
        const apiKey = localStorage.getItem('elevenlabs_api_key');
        if (apiKey) {
            console.log('‚úÖ API Key do Eleven Labs encontrada');
            
            // Verificar se est√° usando a voz correta
            if (window.voiceSystem && window.voiceSystem.elevenLabsTTS) {
                const elevenLabs = window.voiceSystem.elevenLabsTTS;
                console.log('üéØ Configura√ß√£o atual do Eleven Labs:', {
                    normalVoiceId: elevenLabs.voiceIds?.normal,
                    demonicVoiceId: elevenLabs.voiceIds?.demonic,
                    modelId: elevenLabs.modelId
                });
            }
        } else {
            console.log('‚ö†Ô∏è API Key do Eleven Labs n√£o encontrada');
        }
    }
    
    // Adicionar bot√£o de teste de volume
    addVolumeTestButton();
}

